package com.example.user.homework;

public class Food {
    private String n;
    private String a;
    private String p;

    public Food(){
    }

    public Food(String n, String a, String p) {
        this.n = n;
        this.a = a;
        this.p = p;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }


}

